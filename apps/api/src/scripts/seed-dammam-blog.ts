async function seedBlogPosts() {
  await ensurePrismaConnection();

  for (const post of posts) {
    await prisma.blogPost.upsert({
      where: { slug: post.slug },
      update: {
        titleAr: post.titleAr,
        excerptAr: post.excerptAr,
        contentAr: post.contentAr,
        coverImage: post.coverImage,
        seoTitleAr: post.seoTitleAr,
        seoDescriptionAr: post.seoDescriptionAr,
        published: true
      },
      create: {
        ...post,
        published: true
      }
    });
  }
}

async function assignRealProjectCovers() {
  const projects = await prisma.project.findMany({
    select: {
      coverImage: true,
      beforeImage: true,
      afterImage: true,
      gallery: true
    },
    orderBy: { createdAt: "desc" }
  });

  const imagePool = Array.from(
    new Set(
      projects
        .flatMap((project) => [project.coverImage, project.beforeImage, project.afterImage, ...(project.gallery || [])])
        .filter((value): value is string => Boolean(value && value.trim()))
        .map((value) => value.trim())
    )
  );

  if (!imagePool.length) {
    console.log("No project images available yet, keeping generated blog covers.");
    return;
  }

  const seededSlugs = posts.map((post) => post.slug);
  const seededPosts = await prisma.blogPost.findMany({
    where: {
      slug: {
        in: seededSlugs
      }
    },
    select: { id: true, slug: true }
  });
  const seededBySlug = new Map(seededPosts.map((post) => [post.slug, post.id]));

  let assigned = 0;

  for (let index = 0; index < posts.length; index += 1) {
    if (index >= imagePool.length) {
      break;
    }

    const targetPostId = seededBySlug.get(posts[index].slug);

    if (!targetPostId) {
      continue;
    }

    await prisma.blogPost.update({
      where: { id: targetPostId },
      data: { coverImage: imagePool[index] }
    });

    assigned += 1;
  }

  console.log(`Assigned ${assigned} unique blog covers from project images.`);
}

seedBlogPosts()
  .then(async () => {
    await assignRealProjectCovers();
    const count = await prisma.blogPost.count();
    console.log(`Seed complete. Blog posts in database: ${count}`);
    await disconnectPrisma();
  })
  .catch(async (error) => {
    console.error("Seed failed:", error);
    await disconnectPrisma();
    process.exit(1);
  });
