import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

function normalizeSha(value) {
  const candidate = String(value || "").trim();
  return /^[a-f0-9]{40}$/i.test(candidate) ? candidate.toLowerCase() : "";
}

function resolveCommitSha() {
  // Prefer git metadata from the checked-out source to avoid stale env values.
  try {
    const gitSha = execSync("git rev-parse HEAD", { stdio: ["ignore", "pipe", "ignore"] })
      .toString()
      .trim();
    const normalizedGitSha = normalizeSha(gitSha);
    if (normalizedGitSha) {
      return normalizedGitSha;
    }
  } catch {
    // Fall back to CI-provided commit variables when git metadata is unavailable.
  }

  const envCandidate =
    normalizeSha(process.env.GIT_COMMIT_SHA) ||
    normalizeSha(process.env.RAILWAY_GIT_COMMIT_SHA) ||
    normalizeSha(process.env.GITHUB_SHA);

  if (envCandidate) {
    return envCandidate;
  }

  return "unknown";
}

const commitSha = resolveCommitSha();
const outputDir = path.resolve(process.cwd(), "src", "generated");
const outputFile = path.join(outputDir, "build-version.ts");

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(
  outputFile,
  [
    "// Auto-generated by apps/api/scripts/write-build-version.mjs",
    `export const BUILD_COMMIT_SHA = ${JSON.stringify(commitSha)};`,
    ""
  ].join("\n"),
  "utf8"
);

console.log(`[build-version] commit=${commitSha}`);
